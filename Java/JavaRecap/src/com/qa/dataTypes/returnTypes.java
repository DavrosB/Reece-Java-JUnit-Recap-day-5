package com.qa.dataTypes;

public class returnTypes {
	
	// When creating a method, sometimes a return type is produced. These are the data types
	
	// Specified it will return a String
	public static String stringReturn() {
		return "Hey everyone!";
	}
	
	// Create a method that returns an int 
	public static String intReturn() {
		return "123";
	}
	
	// Creates a method that returns a boolean
	public static boolean boolReturn() {
		return true;
	}
}
